object Cond {
    def main(args: Array[String]) {
        var x = 30;

        if(x == 10) {
            println("Valor de x é igual a 10");

        } else if(x == 20) {
            println("Valor é igual a 20");
       
        } else if (x == 30) {
            println("Valor é igual a 30");

        } else {
            println("Valor desconhecido");
        }
    }
}